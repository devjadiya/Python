# Slicing
b = "Hello, World!" #Specify the start index and the end index, separated by a colon, to return a part of the string.
print(b[2:5])

# Slicing from the Start
b = "Hello, World!"
print(b[:5])

# Slicing to the end
b = "Hello, World!"
print(b[2:])

# Negative Indexing
b = "Hello, World!"
print(b[-5:-2])
